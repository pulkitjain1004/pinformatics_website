			<p>The Healthcare Transformation and Quality Improvement Program (Program) aims to improve access to health care, increase quality of care, and reduce costs of care by expanding Medicaid Managed Care, revising the Uncompensated Care (UC) system, and creating a Delivery System Reform Incentive Pool (DSRIP). Given the geographic vastness of the State of Texas, as well as the diversity of the populations in different areas of the state, the Texas Health and Human Services Commission (HHSC) elected to implement the UC and DSRIP portions of the Program by facilitating the creation of 20 Regional Healthcare Partnerships (RHPs). The RHPs serve as a mechanism to plan, implement, and track DSRIP projects. In many cases, the counties and subsequent agencies and providers comprising these RHPs have worked together previously in varying capacities; however, as the RHP regions do not reflect exact boundaries of other service region designations, new stakeholders were likely introduced as well.</p>
<p>Through the RHPs, the Program seeks to create collaborative environments for providers to achieve improved system performance. In the Program, Upper Payment Limit (UPL) funds have been replaced with a UC pool, and hospitals must now participate in RHPs to access those funds. Hospitals and other participating providers may also earn DSRIP for collaborative projects designed to improve system performance in various specific ways approved by HHSC and the Centers for Medicare &amp; Medicaid Services (CMS). Such DSRIP activity is expected to improve access to preventive care; improve quality, health, and cost outcomes for the populations served by specific projects; and improve regional health and human service delivery capacity through enhanced collaboration.</p>
<p>To evaluate the Program, we will assess the following questions. Below these questions are reordered from that in the CMS-approved evaluation plan to a sequence that we believe reflects the Program’s implicit theory of change, from system restructuring to delivery innovation to improved health and cost outcomes:</p>
<p>What are stakeholders’ experiences and perceptions about the implementation and effectiveness of the Program, and what are their recommendations for improving it in the future? (Evaluation Goals 10 &amp; 11; STC 69.a.v)</p>
<ol>
<li>Did the Program increase collaboration among entities that comprised the RHPs? (Evaluation Goal 9; STC 68.a.iv)</li>
<li>Did DSRIP projects improve value (cost, quality, health outcomes)? (Evaluation Goals 6,7 &amp; 8; STC 68 a.iii)</li>
<li>Did participation in DSRIP projects change the trajectory of UC, the other part of the former UPL pool? (5; STC 68.a.iv)</li>
</ol>

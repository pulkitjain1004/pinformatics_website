				<h2 style="margin: 0">The North Carolina Public Health Practice Based Research Network (PBRN) Research Implementation Award : Reimbursement Policy Change and Its Effect on Local Public Health Service Outcomes</h2>
				<p>Funding : Robert Wood Johnson Foundation (March 2010-Aug 2012) &amp; Health Resources and Services Administration, Maternal and Child Health Bureau, US-DHHS (Mar 2011-Aug 2012)</p>
				<ul style="margin-bottom: 50px">
					<li>Collaboration with School of Public Health</li>
					<li>Administrative Data : NC LHD data (HSIS/HIS), vital statistics, Mediciad, WIC, Pregnancy Nutritional Surveillance System</li>
					<li>The purpose of the proposed two-year project is to conduct public health systems and services research on a key policy change recently enacted in North Carolina (NC) that affects the staffing and delivery of services in local health departments (LHDs) throughout the state. The proposed project will examine the consequences of a recent reduction of Medicaid reimbursement funding for a program that since the late 1980s has provided evidence-based case management, maternity outreach and postpartum services to low-income women and their children, and contributed to improved birth outcomes in these at-risk populations.</li>

				<h2 style="margin: 0">NuTRUtion Nation : The Truth About Our Food Environment</h2>
				<p>Funding : Food Research Group, Carolina Population Center (May 2012-present)</p>
				<ul style="margin-bottom: 50px">
					<li>Collaboration with Department of Nutrition, School of Public Health</li>
					<li>While people care about health and nutrition and make numerous decisions about food daily, they are often overwhelmed by conflicting messages and marketing. This project will use crowdsourcing to gather, organize and share up-to-date objective nutrition facts label and ingredient information from foods in people’s daily environment. This will help public health researchers and consumers understand the spatial and temporal variations in the types and nutrition quality of foods around them. NuTRUtion Nation is a tool that can empower communities to document and monitor changes in their food environment and advocate for improvements.</li>

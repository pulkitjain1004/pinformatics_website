				<h1>About Us</h1>	
				<p class="lead"></p>
				<p>The Population Informatics Research Group applies informatics, data science, and computational methods to the increasingly large digital traces available to advance public health, social science, and population research. This research group is a joint effort between UNC-CH and Texas A&amp;M (TAMU). We currently collaborate with the Renaissance Computing Institute, Carolina Population Center, The Odum Institute, the Gillings School of Global Public Health, and the School of Social Work at UNC and the School of Public Health and the Department of Computer Science at Texas A&amp;M. We specialize in data science, KDD (Knowledge Discovery and Datamining), data integration, visualization, decision support systems, health informatics, computational social science, and privacy.</p>
				<p>Our research is an emerging research area at the intersection of domain science (social, behavioral, economic, and health sciences), computer science, and statistics in which quantitative methods and computational tools are applied to big data about people to answer social science questions. Health informatics is a major subarea focusing on big data about health. We generally consider all research using large data sets where the unit of analysis is a person or groups of people (family, organizations, etc) as being relevant. Some examples are health informatics using hospital records, decision support systems based on administrative data for government agencies, public computational journalism using government data, and behavioral research using purchasing data.</p>
				<p>Our primary home is the department of computer science with most of our students recruited from the department. We collaborate with many different organizations who work with big data about people. Dr. Hye-Chung Kum has a ph.D. in computer science as well as an MSW and holds an appointment in both the computer science department (UNC-CH) and the school of public health (TAMU). Dr. Stanley Ahalt is the director of RENCI (Renaissance Computing Institute), director of the Biomedical Informatics Core at NC-TraCS, and a full professor in the computer science department. We also work with the school of social work as well as other groups within and outside the school. We welcome collaborations with other researchers interested in population informatics, computational social science, computational journalism, and health informatics. Please contact Hye-Chung Kum (kum at tamhsc dot edu) if you have any questions.</p>

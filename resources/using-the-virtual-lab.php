				<h1>Using the Virtual Lab</h1>	
				<p>You can use the TAMU main campus VOAL (virtual open access lab) for this class from any windows or apple machine.</p>
				<ul>
					<li>What you get
					<ul>
						<li>SAS software</li>
						<li>1G of diskspace that you can use over the full course. I suggest not putting any sensitive data from projects on this disk, but public data for class is fine.</li>
					</ul>
					</li>
					<li>What you have to do
						<ul>
							<li>activate your NetID (http://gateway.tamu.edu/)</li>
							<li>Go to https://voal.tamu.edu/ (you have to use IE or Safari)</li>
							<li>Download and install a small software (this should be VERY easy)</li>
							<li>Reboot your computer, and go back to https://voal.tamu.edu/</li>
							<li>Log in with your NetID</li>
							<li>You should get your very own desktop (H: is your 1G of space)</li>
							<li>Click on the start button, and type in ‘sas’ in the find programs box</li>
							<li>select SAS 9.3 (English)</li>
						</ul>
					</li>
					<li>For more information</li>
					<ul>
						<li>http://oal.tamu.edu/</li>
						<li>Main campus help page (24/7/365): http://hdc.tamu.edu/index.php</li>
					</ul>

				</ul>


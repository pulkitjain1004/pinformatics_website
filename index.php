<h2><a href="http://research.tamhsc.edu/pinformatics/wp-content/uploads/sites/2/2016/03/pinfo.pdf">What is Population Informatics?</a></h2>
Computational Social Science is an emerging research area at the intersection of health science, social sciences, computer science, and statistics in which quantitative methods and computational tools are applied to big data about people to answer social science questions. Broadly speaking there are two approaches as follows:
<ul>
  <li>Population Informatics : The systematic study of populations via secondary analysis of massive data collections (termed “big data”) about people. In particular, we focus most on improving <em>health</em> outcomes for a population and the <b> data science approach</b> which is about generating actionable information from raw data. Another important aspect of population informatics is <em>Public health informatics </em> which is more about how to best utilize the information generated using data science to improve public health.</li>
  <li>Simulations (i.e., Agent Based Modeling (ABM) ) : Discover useful information and knowledge about our society through simulating the actions and interactions of autonomous agents (individuals and groups/organizations). Many of the parameters to model autonomous agents come from Population Informatics research.</li>
</ul>
<h2>Our Social Genome : A Federated Data System of Digital Data about People</h2>
New scientific opportunities are emerging as a result of increasingly effective data organization, access, and usage. Many fields of study have been transformed to a new level by new tools and data infrastructure. For example, the analysis of DNA sequence data has transformed medical research. We need to push the frontier of social sciences by doing the same with digital data available about our society; this will enable us to gain fundamental insights into the many facets of our society. A key source of information about all aspects of our society resides in government administrative data and various private operational data. From the day we are born until our death, most all of our activities leave footprints in various digital data systems. Birth, marriage, and death certificates are filed with the government, education records remain with departments of public instruction, and traces of employment can be found in the ESC UI (Employment Security Commission Unemployment Insurance) wage data. Without a doubt, a well-integrated data system that can encompass much of the data systems will hold the footprints of our society, our <strong>social genome</strong>. The two main hurdles to building such a system to transform the social sciences are (1) privacy concerns and the laws in place to protect individual confidentiality, and (2) the physiology of administrative data, which is fragmented, short-lived, and sometimes has questionable reliability. Our group’s research focuses on resolving these two barriers to building a federated data system of digital data about people for research. Once resolved, we can build the social genome data infrastructure that could finally allow us to move toward understanding how current policies play out in our society and how to make informed policies using information and knowledge gathered from these digital traces. Together, our digital traces collectively capture the footprints of our society. Like the human genome, the social genome data has much buried in the massive almost chaotic data. If properly analyzed and interpreted, this social genome could offer crucial insights into many of the most challenging problems facing our society (i.e. affordable and accessible quality healthcare, economics, education, employment, and welfare) <a href="http://pinformatics.web.unc.edu/social-genome/">For more details ... </a>

<h2>Welcome</h2>
The Population Informatics Research Group applies informatics, data science, and computational methods to the increasingly large digital traces available to advance public health, social science, and population research. This research group is a joint effort between UNC-CH and Texas A&amp;M (TAMU). We currently collaborate with the Renaissance Computing Institute, Carolina Population Center, The Odum Institute, the Gillings School of Global Public Health, and the School of Social Work at UNC and the School of Public Health and the Department of Computer Science at Texas A&amp;M. We specialize in data science, KDD (Knowledge Discovery and Datamining), data integration, visualization, decision support systems, health informatics, computational social science, and privacy. <a href="http://pinformatics.web.unc.edu/">UNC-CH Population Informatics Research Group Website</a>
